# random 随机，是Python语言提供的内置模块，不需要额外下载的模块，语言自带的

# 1.导入random模块
import random

# 2.使用random模块调用一个randint(a,b)方法，需要传递两个参数
# a,b 是两个整型数值，表示一个范围，如果书写（1,3）表示这个方法会随机生成1,2,3,之间的任意一个整型数值
number = random.randint(1, 3)

# 3.查看生成的数值
print(f'number = {number}')

